int	ft_atoi(const char *nptr)
{
	int	neg;
	int	nb;

	nb = 0;
	neg = 1;
	while (*nptr == 32 || (*nptr >= 8 && *nptr <= 13))
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			neg *= -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		nb *= 10;
		nb += *nptr - 48;
		nptr++;
	}
	return (nb * neg);
}
