#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	char	*src;
	int		i;

	i = 0;
	src = (char *) s;
	dest = malloc(sizeof(char) * ft_strlen(src) + 1);
	if (dest == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
